    code: str
    display_name: str
    item_word: str  # 顾客“消费的东西”叫什么：菜品 / 项目 / 发型 / 款式
    aliases: Tuple[str, ...]
    block: str  # 渲染进 writer prompt 的行业内容约束
    few_shots: Tuple[str, ...] = field(default_factory=tuple)  # 空则回退用平台 few_shots


