"""行业/店铺类型约束层。

平台约束（platforms/）管「在哪个平台、什么格式」，本层管「什么类型的店、聊什么内容」。
按 store.industry_type（自由文本，如「川菜/餐饮」「足疗按摩」「美甲店」）模糊匹配。

隔离机制（三重）：
1. 结构隔离：评价池按 store_id 分池，不同店物理上不串（在 Go 侧）。
2. 生成隔离：本层把平台规则里的「菜品」reframe 成各行业的项目/款式，禁跨行业词汇。
3. 串味兜底：signature_terms + find_cross_industry_leak，生成后扫到别的行业的标志词即重写。
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import List, Tuple


@dataclass(frozen=True)
class IndustrySpec:
    code: str
    display_name: str
    item_word: str  # 顾客“消费的东西”叫什么：菜品 / 项目 / 款式
    aliases: Tuple[str, ...]
    block: str  # 渲染进 writer prompt 的行业内容约束
    # 本行业独有、其它行业里出现就属串味的标志词（用于跨行业泄漏检测，需高区分度）
    signature_terms: Tuple[str, ...] = field(default_factory=tuple)
    few_shots: Tuple[str, ...] = field(default_factory=tuple)  # 空则用平台 few_shots


