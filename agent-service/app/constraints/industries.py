# 匹配顺序：先专门行业，最后兜底餐饮。
# PET 排在 BEAUTY 前：避免「宠物美容」被 BEAUTY 的「美容」抢匹配（美容美发/美容美甲已由更前的 hair/nail 处理）。
_ORDERED = (
    FOOTMASSAGE, HAIRSALON, NAILSALON, PET, BEAUTY, FITNESS,
    ENTERTAINMENT, AUTO, RESTAURANT,
)
_ALL = _ORDERED  # 含 RESTAURANT


def match_industry(industry_type: str) -> IndustrySpec:
    text = (industry_type or "").strip().lower()
    if text:
        for spec in _ORDERED:
            if any(alias.lower() in text for alias in spec.aliases):
                return spec
    return RESTAURANT


def find_cross_industry_leak(content: str, target: IndustrySpec) -> List[str]:
    """扫描文本里是否混入了「别的行业」的标志词。返回命中的串味词列表（空=干净）。

    只用高区分度的 signature_terms，避免误伤“服务/环境”这类通用词。
    """
    hits: List[str] = []
    for spec in _ALL:
        if spec.code == target.code:
            continue
        for term in spec.signature_terms:
            if term and term in content and term not in hits:
                hits.append(term)
    return hits


def reviewer_industry_note(spec: IndustrySpec) -> str:
    return (
        f"本店行业：{spec.display_name}。正文应围绕「{spec.item_word}」及对应体验展开；"
        f"若出现与本行业无关的内容（如足疗 / 美甲店写菜品口味），视为跑题，扣真实性与合规分。"
    )
