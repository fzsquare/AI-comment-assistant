"""去 AI 写作痕迹（humanizer），适配中文评价。

基于 Wikipedia「Signs of AI writing」，只取跨语言适用的部分，并按中文评价场景改写。
不照搬英文专属规则（curly quotes / title case / 英文 AI 词）；emoji 不在此禁，交由
平台人设按满意度控制（小红书允许少量 emoji）。

三处使用：
- humanizer_block()：写进 writer system prompt，从源头避免 AI 腔。
- REVIEWER 提示里引用同一套清单：评审在「拟人化」维度查 AI 腔。
- find_ai_tells()：保守的确定性兜底检测，命中即逼一次「去 AI 腔」重写。
"""
from __future__ import annotations

from typing import List

# 高置信、真人很少这么写的「套话/拔高」短语。保守，避免误伤口语化真人评价。
AI_TELL_PHRASES: List[str] = [
    # 万能拔高结尾 / 总结套话
    "总而言之", "综上所述", "总的来说", "不虚此行", "不容错过",
    "意犹未尽", "回味无穷", "令人难忘", "难以忘怀", "无可挑剔",
    "完美无缺", "一应俱全", "应有尽有", "名不虚传", "堪称一绝",
    "人间值得",
]

# 破折号滥用是最稳的 AI tell 之一；中文写成「——」。真人随手写的评价极少用。
AI_TELL_DASH = "——"


def find_ai_tells(text: str) -> List[str]:
    """返回文本中命中的高置信 AI 写作痕迹。空列表 = 没明显 AI 腔。"""
    hits = [w for w in AI_TELL_PHRASES if w in text]
    if AI_TELL_DASH in text:
        hits.append("破折号——")
    return hits


def humanizer_block() -> str:
    """渲染进 writer system prompt 的「去 AI 腔」约束段。"""
    return (
        "【去 AI 腔（强制，让文案像真人随手写、不像 AI 拼的）】\n"
        "- 别三连排比：不要“味道好、环境好、服务好”这种三件套；真人详略不均，只重点说一两样。\n"
        "- 别对仗升华：避免“不仅…而且…”“既…又…”“不只是一顿饭，更是一种体验”这类拔高句式。\n"
        "- 别堆华丽空词：少用“惊艳/完美/回味无穷/令人难忘/无可挑剔/宝藏”等空泛赞美，用具体细节替代。\n"
        "- 别套话结尾：不要“总之很满意/不虚此行/值得一试/期待下次再来”这种万能收尾；"
        "用一句具体的话收（如“下次想带我妈来尝尝那个鱼”）。\n"
        "- 别用破折号“——”：改用逗号或句号。\n"
        "- 句子长短交错：长一句短一句混着写，别每句一样长、节奏太工整。\n"
        "- 允许一点“毛刺”：口语、稍微跑题的小插叙、半句吐槽，反而更像真人。\n"
        "- 别像“介绍”店：少用“坐落于/拥有/堪称/作为一家…”，直接说你的真实体验。"
    )


def reviewer_humanizer_note() -> str:
    """渲染进 reviewer system prompt 的「去 AI 腔核查」段（计入拟人化扣分）。"""
    return (
        "【去 AI 腔核查（计入‘拟人化’扣分，命中请在 issues 指出并给改法）】\n"
        "出现以下任一即判为有 AI 写作痕迹、扣拟人化分：三连排比；对仗/升华句"
        "（不仅…而且…、不只是…更是…）；华丽空词堆砌（惊艳/完美/回味无穷/令人难忘）；"
        "万能套话结尾（总之/不虚此行/值得一试/期待下次）；破折号“——”；每句长度雷同、"
        "节奏过于工整。读起来要像真人随手写，不像 AI 拼的。"
    )
