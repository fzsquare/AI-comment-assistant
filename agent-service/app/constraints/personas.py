"""第四部分：通用拟人化约束（所有平台 writer 共用）。"""
from __future__ import annotations

from typing import Dict

# 4.1 身份锚定
IDENTITY_ELEMENTS = {
    "职业身份": ["打工人", "学生党", "宝妈", "自由职业", "程序员", "教师"],
    "地域身份": ["本地人", "北漂", "深漂", "新上海人", "旅游者"],
    "消费身份": ["第一次来", "老顾客", "朋友推荐", "路过尝试"],
    "关系身份": ["带爸妈", "带娃", "闺蜜", "情侣", "同事", "一个人"],
}

# 4.3 情感强度 ↔ 满意度（正面词比例 / 感叹句 / 表情符号）
EMOTION_BY_SATISFACTION: Dict[str, Dict[str, str]] = {
    "非常满意": {"正面词比例": "85-95%", "感叹句": "2-3个", "表情": "2-3个"},
    "比较满意": {"正面词比例": "70-85%", "感叹句": "1-2个", "表情": "1-2个"},
    "一般": {"正面词比例": "45-55%", "感叹句": "0-1个", "表情": "0-1个"},
    "有点失望": {"正面词比例": "25-35%", "感叹句": "0个", "表情": "0个"},
    "非常失望": {"正面词比例": "10-20%", "感叹句": "0个", "表情": "0个"},
}

# 4.5 / 1.4 缺点披露 ↔ 满意度
DEFECT_BY_SATISFACTION: Dict[str, Dict[str, str]] = {
    "非常满意": {"数量": "1个", "严重程度": "轻微", "位置": "结尾段"},
    "比较满意": {"数量": "1-2个", "严重程度": "轻微-中等", "位置": "中后段"},
    "一般": {"数量": "2-3个", "严重程度": "中等", "位置": "贯穿全文"},
    "有点失望": {"数量": "2-3个", "严重程度": "中等-严重", "位置": "贯穿全文"},
    "非常失望": {"数量": "3个以上", "严重程度": "严重", "位置": "贯穿全文"},
}

# 缺点类型（按安全度排序）
DEFECT_TYPES = [
    "性价比类（略贵/性价比一般，不写具体金额）",
    "位置类（有点偏/停车困难/地铁要走一段）",
    "等待类（饭点人多/上菜慢/等位）",
    "环境类（有点吵/座位挤/空调太冷）",
    "产品类（分量一般/口味偏咸/选择不多）",
]

# 4.4 口语化
COLLOQUIAL = {
    "评价词": ["真香", "没毛病", "还行", "可以", "不错"],
    "称呼词": ["姐妹", "宝子", "家人们", "友友们"],
    "语气词": ["嘛", "哈", "啦", "呗", "咯", "呀", "哦", "呢", "吧"],
    "连接词": ["然后", "就是", "其实", "说实话", "讲真"],
}


def persona_block(satisfaction: str) -> str:
    """渲染进 writer system prompt 的拟人化约束段。"""
    emo = EMOTION_BY_SATISFACTION.get(satisfaction, EMOTION_BY_SATISFACTION["比较满意"])
    dfc = DEFECT_BY_SATISFACTION.get(satisfaction, DEFECT_BY_SATISFACTION["比较满意"])
    return (
        "【拟人化约束（强制）】\n"
        "- 身份锚定：至少自然带出 1 个身份（如“带爸妈”“学生党”“老顾客”），"
        "禁止生硬插入（不要写“我是一个打工人，我来吃饭”）。\n"
        "- 细节具象：用具体时间/数字/人物，禁止模糊词。"
        "✓“上周三下午”“等了十来分钟”“服务员小哥”；✗“有一天”“花了些钱”“服务员”。\n"
        f"- 情感强度匹配满意度【{satisfaction}】：正面词约 {emo['正面词比例']}，"
        f"感叹句 {emo['感叹句']}，表情 {emo['表情']}。禁止“绝绝子/YYDS/天花板”式浮夸。\n"
        f"- 缺点披露：写 {dfc['数量']}（{dfc['严重程度']}），位置在{dfc['位置']}。"
        f"优先选：{DEFECT_TYPES[0]}、{DEFECT_TYPES[1]}、{DEFECT_TYPES[2]}。"
        "委婉表达（“唯一小缺点是…”“美中不足的是…”），禁止“太差了/千万别来”。\n"
        "- 口语化：适度用语气词（嘛/啦/哈/吧）和连接词（其实/说实话），像真人随口说，"
        "不要书面语，也不要过度网络化。\n"
        "- 真实底线（强制，违反直接判不合规）：①店名只作为核查数据，正文不要出现店名，严禁改名、"
        "编造分店或品牌名；②严禁编造具体路名/门牌/地标/分店位置；地址/商圈只有门店信息"
        "提供时才能写，未提供地址时也不要写暗示城市的身份（如“新上海人/北漂”）；"
        "③只能写“可用关键词”里的菜品，严禁编造店里没有的菜。"
        "注意：时间/同行人/心情这类“场景”可以合理虚构以保证多样性，但门店身份、地址、菜品三者必须真实。"
    )
